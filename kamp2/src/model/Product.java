package model;

/*
    - class adları, PascalCase şeklinde yazılır.
    - field adları camelCase şeklinde yazılır.
*/
public class Product {

    public String name;           // String veri tipinden, ismim olsun.
    public double unitPrice;      // double veri tipinden, birim fiyatım olsun.
    public double discount;       // double veri tipinden, indirim oranım olsun.
    public String imageUrl;       // Tuttuğum nesnenin String veri tipinden sadece bir resimUrl olsun.
    //String [] imageUrl;         // Tuttuğun nesnenin birden fazla resmi olabilir. Onun için array oluşturabiliriz.
    public int unitsInStock;      // int veri tipinden stok adeti ;


}
